# Выводит пустую строку (функция print без аргументов)
print()

# Выводит тип данных 'a' — это строка (тип <class 'str'>)
print(type('a'))  # <class 'str'>

# Выводит тип данных "b" — это также строка
# (тип <class 'str'>)
print(type("b"))  # <class 'str'>

# Выводит тип данных '''c''' — это тоже строка
# (тип <class 'str'>)
print(type('''c'''))  # <class 'str'>

# Преобразует число 98.6 в строку и выводит её
print(str(98.6))  # '98.6'

# Преобразует число 1.0e4 (10000.0) в строку и выводит её
print(str(1.0e4))  # '10000.0'

# Преобразует логическое значение True
# в строку и выводит её
print(str(True))  # 'True'

# Выводит строку с переносом на новую
# строку после 'A man.'
print('A man.\n P')  # A man.
                     #  P

# Конкатенирует строки 'a' и 'b' и выводит результат
print('a' + 'b')  # 'ab'

# Повторяет строку 'a' 4 раза и выводит результат
print('a' * 4)  # 'aaaa'

# Выводит первый символ строки 'ab'
# (индексация начинается с 0)
print('ab'[0])  # 'a'

# Выводит всю строку 'abc' (срез [:]
# возвращает всю строку)
print('abc'[:])  # 'abc'

# Выводит подстроку, начиная с индекса 1 до конца строки
print('abc'[1:])  # 'bc'

# Выводит подстроку из последних двух символов
print('abc'[-2:])  # 'bc'

# Выводит каждый второй символ строки,
# начиная с первого
print('abcdefj'[::2])  # 'acej'

# Выводит длину строки 'abc'
print(len('abc'))  # 3

# Объединяет список строк ['a', 'b']
# в одну строку без разделителя
print(''.join(['a', 'b']))  # 'ab'

# Объединяет список строк ['a', 'b']
# в одну строку с разделителем ','
print(','.join(['a', 'b']))  # 'a,b'

# Разделяет строку 'abc' по пробелам
# (но пробелов нет, поэтому возвращает список с одной строкой)
print('abc'.split())  # ['abc']


