# Создаем строку 'aoiu'
a1 = 'aoiu'

# Проверяем, содержится ли символ 'a' в строке a1
print('a' in a1)  # True, так как 'a' есть в строке 'aoiu'

# Заменяем символ 'a' на 'c' в строке 'ab'
print('ab'.replace('a', 'c'))  # 'cb'

# Удаляем пробелы в начале и конце строки ' abc '
print(' abc '.strip())  # 'abc'

# Проверяем, начинается ли строка 'a' с символа 'a'
print('a'.startswith('a'))  # True

# Проверяем, заканчивается ли строка 'a' на символ 'a'
print('a'.endswith('a'))  # True

# Центрируем строку 'a' в строке длиной 3,
#добавляя пробелы по бокам
print('a'.center(3))  # ' a '

print('a'.ljust(5, '-'))  # 'a----'
print('a'.rjust(5, '-'))  # '----a'

# Форматируем строку, подставляя 'str' вместо {}
print('{}'.format('str'))  # 'str'

text = "  abc  "
print(text.lstrip())
# 'abc  ' (удаляет пробелы слева)
print(text.rstrip())
# '  abc' (удаляет пробелы справа)

print('{} {}'.format('Hello', 'world'))  # 'Hello world'

print('a'.isupper())
# False (проверка, является ли символ заглавным)
print('A'.isupper())
# True
print('a'.islower())
# True (проверка, является ли символ строчным)

text = "Hello"
print(text[::-1])  # 'olleH'

text = "Hello, world!"
print(text.count('l'))  # 3

print('abc'.isalpha())  # True
print('abc123'.isalpha())  # False