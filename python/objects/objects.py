a = {}
print(a) # {}







a = {
    'a': 1,
    'b': 2
    }

print(a) # {'a': 1, 'b': 2}

a = {
    1: 'a',
    2: 'b'
    }
print(a) # {1: 'a', 2: 'b'}





a = [['a', 'b'], ['c', 'd']]
print(dict(a)) # {'a': 'b', 'c': 'd'}




a = [('a', 'b'), ('c', 'd')]
print(dict(a)) # {'a': 'b', 'c': 'd'}


a = ['ab', 'cd']
print(dict(a)) # {'a': 'b', 'c': 'd'}

a = ('ab', 'cd')
print(dict(a)) # {'a': 'b', 'c': 'd'}






