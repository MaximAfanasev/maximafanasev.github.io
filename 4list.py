# Создание пустого списка
a2 = []
print(a2)  # []

# Создание пустого списка с помощью list()
b1 = list()
print(b1)  # []

# Преобразование строки в список
print(list('abc'))  # ['a', 'b', 'c']

# Доступ к элементу списка по индексу
print(['a', 'b'][1])  # 'b'

# Умножение списка на число
print([1] * 3)  # [1, 1, 1]

# Конкатенация списков
print([1] + [2])  # [1, 2]

# Срезы списка
print(['a', 'b', 'c'][1:])  # ['b', 'c']
print(['a', 'b', 'c'][-1:])  # ['c']
print(['a', 'b', 'c'][:1])  # ['a']
print(['a', 'b', 'c'][:-2])  # ['a']

# Разделение строки на список
print('a,b,c'.split(','))  # ['a', 'b', 'c']

# Удаление элемента из списка
c1 = ['a', 'b']
c1.remove('a')
print(c1)  # ['b']

# Добавление элемента в конец списка
d1 = [1, 2, 3]
d1.append(4)
print(d1)  # [1, 2, 3, 4]

# Расширение списка другим списком
e1 = [1]
f1 = [2]
e1.extend(f1)
print(e1)  # [1, 2]

# Изменение части списка
j1 = [1, 2, 3, 4]
j1[1:3] = [5, 6]
print(j1)  # [1, 5, 6, 4]

# Удаление элемента по значению
k1 = [1, 2, 3]
k1.remove(2)
print(k1)  # [1, 3]

# Удаление последнего элемента
l1 = [1, 2, 3]
l1.pop()
print(l1)  # [1, 2]

# Очистка списка
p1 = [1, 2, 3]
p1.clear()
print(p1)  # []

# Поиск индекса элемента
print([1, 2].index(2))  # 1

# Проверка наличия элемента в списке
print(1 in [1, 2])  # True

# Подсчет количества вхождений элемента
print([1, 1, 1].count(1))  # 3

# Сортировка списка
q1 = [3, 2, 1]
q1.sort()
print(q1)  # [1, 2, 3]

# Итерация по нескольким спискам одновременно
for _ in zip([1, 2, 3], [4, 5, 6]):
    print(_)  # (1, 4), (2, 5), (3, 6)
		
		
		
		
		
		
# append(item) - добавляет элемент в конец списка
a = [1, 2]
a.append(3)
print(a)  # [1, 2, 3]

# clear() - очищает список
b = [1, 2, 3]
b.clear()
print(b)  # []

# copy() - возвращает копию списка
c = [1, 2, 3]
d = c.copy()
print(d)  # [1, 2, 3]

# count(item) - возвращает количество вхождений элемента
e = [1, 1, 2, 3]
print(e.count(1))  # 2

# extend(iterable) - добавляет элементы из итерируемого объекта в конец списка
f = [1, 2]
f.extend([3, 4])
print(f)  # [1, 2, 3, 4]

# index(item) - возвращает индекс первого вхождения элемента
g = [1, 2, 3, 2]
print(g.index(2))  # 1

# insert(index, item) - вставляет элемент по указанному индексу
h = [1, 2, 3]
h.insert(1, 1.5)
print(h)  # [1, 1.5, 2, 3]

# pop([index]) - удаляет и возвращает элемент по индексу (по умолчанию последний)
i = [1, 2, 3]
print(i.pop())  # 3
print(i)  # [1, 2]

# remove(item) - удаляет первое вхождение элемента
j = [1, 2, 3, 2]
j.remove(2)
print(j)  # [1, 3, 2]

# reverse() - разворачивает список
k = [1, 2, 3]
k.reverse()
print(k)  # [3, 2, 1]

# sort(key=None, reverse=False) - сортирует список
l = [3, 1, 2]
l.sort()
print(l)  # [1, 2, 3]

# Дополнительные методы (не встроенные, но полезные):
# 1. Создание списка с помощью генератора списков
m = [x for x in range(5)]
print(m)  # [0, 1, 2, 3, 4]

# 2. Фильтрация списка
n = [1, 2, 3, 4, 5]
filtered = [x for x in n if x > 2]
print(filtered)  # [3, 4, 5]

# 3. Преобразование списка в строку
o = ['a', 'b', 'c']
print(''.join(o))  # 'abc'

# 4. Максимальный и минимальный элементы
p = [1, 2, 3]
print(max(p))  # 3
print(min(p))  # 1

# 5. Сумма элементов
q = [1, 2, 3]
print(sum(q))  # 6

# 6. Длина списка
r = [1, 2, 3]
print(len(r))  # 3